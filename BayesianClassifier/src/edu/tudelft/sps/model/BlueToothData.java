package edu.tudelft.sps.model;

public class BlueToothData {
	
	private double rssi;
	private int beaconID;
	
	
	public double getRSSI() {
		return rssi;
	}
	public void setRSSI(double rSSI) {
		rssi = rSSI;
	}
	public int getbeaconID() {
		return beaconID;
	}
	public void setbeaconID(int iD) {
		beaconID = iD;
	}

}
